export const randomQuestions = () => {
  const N = 5;

  const result = [];
  const copy = [...QUESTIONS];

  for (let i = 0; i < N; i++) {
    const index = Math.floor(Math.random() * copy.length);
    result.push(copy[index]);
    copy.splice(index, 1);
  }

  return result;
};
